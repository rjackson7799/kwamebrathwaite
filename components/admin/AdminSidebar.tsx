'use client'

import { useState } from 'react'
import Link from 'next/link'
import { usePathname, useRouter } from 'next/navigation'

const navItems = [
  { name: 'Dashboard', href: '/admin', icon: DashboardIcon },
  { name: 'Hero Slides', href: '/admin/hero', icon: HeroIcon },
  { name: 'Artworks', href: '/admin/artworks', icon: ArtworkIcon },
  { name: 'Exhibitions', href: '/admin/exhibitions', icon: ExhibitionIcon },
  { name: 'Press', href: '/admin/press', icon: PressIcon },
  { name: 'Inquiries', href: '/admin/inquiries', icon: InquiryIcon },
  { name: 'Content', href: '/admin/content', icon: ContentIcon },
  { name: 'Newsletter', href: '/admin/newsletter', icon: NewsletterIcon },
  { name: 'Media', href: '/admin/media', icon: MediaIcon },
  { name: 'Activity', href: '/admin/activity', icon: ActivityIcon },
]

export function AdminSidebar() {
  const pathname = usePathname()
  const router = useRouter()
  const [loggingOut, setLoggingOut] = useState(false)

  const isActive = (href: string) => {
    if (href === '/admin') {
      return pathname === '/admin'
    }
    return pathname.startsWith(href)
  }

  const handleLogout = async () => {
    setLoggingOut(true)
    try {
      await fetch('/api/admin/auth/logout', { method: 'POST' })
      router.push('/admin/login')
      router.refresh()
    } catch (error) {
      console.error('Logout error:', error)
      setLoggingOut(false)
    }
  }

  return (
    <aside className="fixed inset-y-0 left-0 w-64 bg-black text-white flex flex-col">
      {/* Logo */}
      <div className="p-6 border-b border-white/10">
        <Link href="/admin" className="block">
          <h1 className="text-lg font-semibold">KB Archive</h1>
          <p className="text-sm text-gray-400">Admin Panel</p>
        </Link>
      </div>

      {/* Navigation */}
      <nav className="flex-1 px-3 py-4 space-y-1 overflow-y-auto admin-sidebar-scrollbar">
        {navItems.map((item) => {
          const active = isActive(item.href)
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`
                flex items-center gap-3 px-3 py-2 rounded-md text-sm font-medium transition-colors
                ${active
                  ? 'bg-white/20 text-white'
                  : 'text-gray-300 hover:bg-white/10 hover:text-white'
                }
              `}
            >
              <item.icon className="w-5 h-5 flex-shrink-0" />
              {item.name}
            </Link>
          )
        })}
      </nav>

      {/* Footer */}
      <div className="p-4 border-t border-white/10 space-y-2">
        <Link
          href="/"
          target="_blank"
          className="flex items-center gap-2 text-sm text-gray-400 hover:text-white transition-colors"
        >
          <ExternalLinkIcon className="w-4 h-4" />
          View Site
        </Link>
        <button
          onClick={handleLogout}
          disabled={loggingOut}
          className="flex items-center gap-2 text-sm text-gray-400 hover:text-white transition-colors disabled:opacity-50 w-full"
        >
          <LogoutIcon className="w-4 h-4" />
          {loggingOut ? 'Signing out...' : 'Sign out'}
        </button>
      </div>
    </aside>
  )
}

